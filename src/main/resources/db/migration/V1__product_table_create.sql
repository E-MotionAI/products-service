CREATE TABLE users
(
    userId BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
);

CREATE TABLE products
(
    productId BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    isActive BOOLEAN,
    isPremium BOOLEAN,
    userId BIGINT NOT NULL,
    CONSTRAINT fk_product_user FOREIGN KEY (userId) REFERENCES users(userId) ON DELETE CASCADE
);

